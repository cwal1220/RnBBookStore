package Library.dto;

import java.util.Date;

public class NoticeDto {

	private String n_id;		//id
	private String n_title;		//제목
	private String n_cont;		//내용
	private Date n_date;		//작성날짜
	private int n_count;		//조회수
	
	@Override
	public String toString() {
		return "Notice [n_id=" + n_id + ", n_title=" + n_title + ", n_cont=" + n_cont + ", n_date=" + n_date
				+ ", n_count=" + n_count + "]";
	}
	
	public String getN_id() {
		return n_id;
	}
	public void setN_id(String n_id) {
		this.n_id = n_id;
	}
	public String getN_title() {
		return n_title;
	}
	public void setN_title(String n_title) {
		this.n_title = n_title;
	}
	public String getN_cont() {
		return n_cont;
	}
	public void setN_cont(String n_cont) {
		this.n_cont = n_cont;
	}
	public Date getN_date() {
		return n_date;
	}
	public void setN_date(Date n_date) {
		this.n_date = n_date;
	}
	public int getN_count() {
		return n_count;
	}
	public void setN_count(int n_count) {
		this.n_count = n_count;
	}
	
}
